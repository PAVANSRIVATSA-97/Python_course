# Lists in Python - ordered collection of items of any data type
names = ["Pavan", "Srilu", "Anu", "Kalyan"]
print(names)
print(names[2])
names.append("Divya")  # adds Divya to the list
print(names)
names[1] = "Alpha"  # changes Srilu to Alpha
print(names)
# Lists are mutable - can be changed(modified)- unlike strings which are immutable

